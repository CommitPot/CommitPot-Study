#include <stdio.h>

int main() {
    int t, n, dp[10][1001] = {0}, arr[10][5] = {{7, -1, -1, -1, -1},
                                                {2, 4,  -1, -1, -1},
                                                {1, 3,  5,  -1, -1},
                                                {2, 6,  -1, -1, -1},
                                                {1, 5,  7,  -1, -1},
                                                {2, 4,  6,  8,  -1},
                                                {3, 5,  9,  -1, -1},
                                                {0, 4,  8,  -1, -1},
                                                {5, 7,  9,  -1, -1},
                                                {6, 8,  -1, -1, -1}};
    for (int i = 0; i < 10; i++) dp[i][1] = 1;
    for (int i = 2; i <= 1000; i++) {
        for (int j = 0; j < 10; j++) {
            for (int k = 0; arr[j][k] != -1; k++) {
                dp[j][i] = (dp[arr[j][k]][i - 1] + dp[j][i]) % 1234567;
            }
        }
    }
    scanf("%d", &t);
    while (t--) {
        scanf("%d", &n);
        int sum = 0;
        for (int i = 0; i < 10; i++) {
            sum = (sum + dp[i][n]) % 1234567;
        }
        printf("%d\n", sum);
    }
}
